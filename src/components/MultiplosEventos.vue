<template>
    <div>
        <button @click="primeiro(txt1, $event), segundo(txt2 ,$event), terceiro()">Ativar Multi Eventos</button>
        <p>{{ multiplos_eventos }}</p>
    </div>
</template>

<script>
export default {
    name: 'MultiplosEventos',
    data() {
        return {
            multiplos_eventos: "",
            txt1: "Primeiro Evento!",
            txt2: "Segundo Evento!"
        }
    },
    methods: {// eslint-disable-next-line
        primeiro(txt, e) {
            this.multiplos_eventos = txt
        },// eslint-disable-next-line
        segundo(txt, e) {
            setTimeout(()=> this.multiplos_eventos = txt, 500)
        },
        terceiro() {
            setTimeout(() => this.multiplos_eventos = "", 1500)
        }
    }
}
</script>

<style scoped>
    div {
        margin: 100px;
    }
</style>